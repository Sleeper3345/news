<?php

namespace app\helpers;

class SlugHelper
{
    /**
     * @param string $string
     * @return string
     */
    public static function generateString(string $string): string
    {
        $charMap = self::getCharMap();

        $str = mb_convert_encoding($string, 'UTF-8', mb_list_encodings());
        $str = mb_strtolower($str, 'UTF-8');
        $str = str_replace(array_keys($charMap), $charMap, $str);
        $str = preg_replace('/[^\p{L}\p{Nd}]+/u', '-', $str);
        $str = preg_replace('/(' . preg_quote('-', '/') . '){2,}/', '$1', $str);
        $str = trim($str, '-');
        $str = mb_strimwidth($str, 0, 245, "");

        $symbols = '0123456789abcdefghijklmnopqrstuvwxyzABCDEFGHIJKLMNOPQRSTUVWXYZ';
        return $str . '-' . substr(str_shuffle(str_repeat($symbols, ceil(10 / strlen($symbols)))), 1, 10);
    }

